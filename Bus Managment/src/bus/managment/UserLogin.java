    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    try { 
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/busm","root", "");
        String sql = "Select * from user_details where username=? and password=? ";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, jTextField1.getText());
        pst.setString(2, jPasswordField1.getText());
        ResultSet rs = pst.executeQuery();
        if (rs.next()) 
        {   
            dispose();
            UserControlPanel ucp = new UserControlPanel();
            ucp.setVisible(true);
            ucp.setLocationRelativeTo(null);
            
            
            
        } else 
        {
            JOptionPane.showMessageDialog(null,"Pogrešno uneti login podaci.");
            jTextField1.setText(null);
            jPasswordField1.setText(null);
        }
        con.close();
        
    } catch (Exception E)
    {
     
    }
    }//GEN-LAST:event_jButton4ActionPerformed

